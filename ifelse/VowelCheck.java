package ifelse;

public class VowelCheck {
	
	public static void main(String[] args) {
		char ch = 'a';
	
		if(ch=='a'||ch=='e'||ch=='i'||ch=='o'||ch=='u') {
			System.out.println("this is Vowel");
		}else {
			System.out.println("this is Consonant");
		}
	}
}

